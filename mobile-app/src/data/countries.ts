/**
 * Find a country by name (fuzzy match for existing text values).
 * Checks exact match first, then case-insensitive, then partial match.
 */
export function findCountryByName(name: string): Country | undefined {
  if (!name) return undefined;
  const lower = name.toLowerCase().trim();

  // Exact match
  const exact = countries.find((c) => c.name === name);
  if (exact) return exact;

  // Case-insensitive match
  const caseInsensitive = countries.find((c) => c.name.toLowerCase() === lower);
  if (caseInsensitive) return caseInsensitive;

  // Partial match (e.g. "USA" -> "United States", "UK" -> "United Kingdom")
  const aliases: Record<string, string> = {
    usa: 'US',
    uk: 'GB',
    england: 'GB',
    'south korea': 'KR',
    'north korea': 'KP',
    russia: 'RU',
    iran: 'IR',
    laos: 'LA',
    syria: 'SY',
    taiwan: 'TW',
    tanzania: 'TZ',
    vietnam: 'VN',
    venezuela: 'VE',
    bolivia: 'BO',
  };

  const aliasCode = aliases[lower];
  if (aliasCode) return countries.find((c) => c.code === aliasCode);

  // Starts-with match
  const startsWith = countries.find((c) => c.name.toLowerCase().startsWith(lower));
  if (startsWith) return startsWith;

  // Contains match
  return countries.find((c) => c.name.toLowerCase().includes(lower));
}
